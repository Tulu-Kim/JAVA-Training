//변수에 값을 대응하는 방법
public class Ex3 {
	public static void main(String[] ar) {
		int a;//정수형 변수 a를 선언했다.
		float b;
		
		a=(int)123.45f;//강제로 int형으로 바꿔줌//
		b=200;
		System.out.printf("a의 값===>%d \n", a);
		System.out.printf("b의 값===>%f \n", b);
	
		char kk ='a';
		System.out.printf("%d \t",(int)kk);
				
	}
}
	
