package chapter1;

//가감승제에 대한 코딩문//
public class Ex02 {
	public static void main(String[] ar) {
		int a,b,result;
		a=100;
		b=50;
		
		result=a+b;
		System.out.println(a+"+"+b+"="+result);
		result=a-b;
		System.out.println(a+"-"+b+"="+result);
		result=a*b;
		System.out.println(a+"*"+b+"="+result);
		result=a/b;
		System.out.println(a+"/"+b+"="+result);
	}
}
